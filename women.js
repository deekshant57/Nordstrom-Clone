function display(womensData) {
  document.querySelector("#blockelem").innerHTML = "";
  womensData.map(function (elem) {
    var div = document.createElement("div");
    var avatar = document.createElement("img");
    avatar.setAttribute("src", elem.image_url);
    avatar.setAttribute("id", "avatar");
    var h4 = document.createElement("h6");
    h4.textContent = elem.brand;
    h4.setAttribute("class", "productbrand");
    var h5 = document.createElement("p");
    h5.textContent = elem.name;
    h5.setAttribute("class", "productname");
    var btn = document.createElement("button");
    btn.textContent = "Bag ";
    btn.setAttribute("class", "bag");
    btn.addEventListener("click", function () {
      addtobag(elem);
    });
    var btn2 = document.createElement("button");
    btn2.setAttribute("class", "wishlist");
    var i = document.createElement("i");
    i.classList.add("bi", "bi-bag-fill");
    btn.append(i);
    var i2 = document.createElement("i");
    i2.classList.add("bi", "bi-heart-fill");
    btn2.append(i2);
    var cost = document.createElement("h5");
    cost.textContent = `INR ${elem.price}`;
    cost.setAttribute("class", "Productcost");
    var div2 = document.createElement("div");
    var wishList = document.createElement("div");
    wishList.append(btn2);
    btn2.addEventListener("click", function () {
      addtowishlist(elem);
    });
    div2.append(btn, wishList);
    div2.setAttribute("class", "bagAndwishlist");
    var alrtdiv = document.createElement("div");
    alrtdiv.classList.add("d-none", "alert", "alert-success");
    alrtdiv.setAttribute("role", "alert");
    // alrtdiv.innerHTML = "<div>Added Successfully</div>";
    alrtdiv.textContent = "Added Successfully";

    var alrtclosebtn = document.createElement("button");
    alrtclosebtn.setAttribute("type", "button");
    alrtclosebtn.setAttribute("data-bs-dismiss", "alert");
    alrtclosebtn.setAttribute("aria-label", "Close");
    alrtclosebtn.classList.add("btn-close", "ms-1");
    alrtdiv.append(alrtclosebtn);
    alrtclosebtn.addEventListener("click", function () {
      alrtdiv.classList.add("d-none");
      // alrtdiv.style.display = "none";
      console.log("object");
    });

    // <button
    //   type="button"
    //   class="btn-close"
    //   data-bs-dismiss="alert"
    //   aria-label="Close"
    // ></button>;

    // var inr = document.createElement("p");
    // inr.textContent = "INR";
    // var div3 = document.createElement("div");
    // div3.append(inr,cost);
    // div3.setAttribute("class","div3");
    div.append(avatar, h4, h5, cost, div2, alrtdiv);
    document.querySelector("#blockelem").append(div);

    function addtobag(elem) {
      cartarr.push(elem);
      console.log(cartarr);
      // alert("added to cart");
      alrtdiv.classList.remove("d-none");
      // alrtdiv.style.display = "block";
      localStorage.setItem("cartitem", JSON.stringify(cartarr));
    }
  });
}
